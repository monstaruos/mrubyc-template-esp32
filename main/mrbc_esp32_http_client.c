/*! @file
  @brief
  mruby/c HTTPClient class for ESP32
  本クラスはインスタンスを生成せず利用する
  init() にて URL をセットし、以後 invoke() にて URL にアクセスする
  cleanup() にて利用を終了する
*/

#include "mrbc_esp32_http_client.h"

#include <string.h>
#include <stdlib.h>

#include "esp_log.h"
#include "esp_system.h"
#include "esp_event.h"
#include "esp_http_client.h"


static struct RClass* mrbc_class_esp32_httpclient;
static char* tag = "main";
static esp_http_client_handle_t client;


/*! HTTP イベントハンドラ
  各種 HTTP イベントが発生した際に呼び出される
*/
static esp_err_t http_event_handler(esp_http_client_event_t* evt)
{
  switch(evt->event_id) {
    case HTTP_EVENT_ERROR:
      ESP_LOGD(tag, "HTTP_EVENT_ERROR");
      break;

    case HTTP_EVENT_ON_CONNECTED:
      ESP_LOGD(tag, "HTTP_EVENT_ON_CONNECTED");
      break;

    case HTTP_EVENT_HEADER_SENT:
      ESP_LOGD(tag, "HTTP_EVENT_HEADER_SENT");
      break;

    case HTTP_EVENT_ON_HEADER:
      ESP_LOGD(tag, "HTTP_EVENT_ON_HEADER, key=%s, value=%s", evt->header_key, evt->header_value);
      break;

    case HTTP_EVENT_ON_DATA:
      ESP_LOGD(tag, "HTTP_EVENT_ON_DATA, len=%d", evt->data_len);
      if (!esp_http_client_is_chunked_response(evt->client)) {
        printf("%.*s", evt->data_len, (char*)evt->data);
      }
      break;

    case HTTP_EVENT_ON_FINISH:
      ESP_LOGD(tag, "HTTP_EVENT_ON_FINISH");
      break;

    case HTTP_EVENT_DISCONNECTED:
      ESP_LOGD(tag, "HTTP_EVENT_DISCONNECTED");
      break;
  }

  return ESP_OK;
}


/*! メソッド init() 本体 : wrapper for esp_http_client_init

  @param url URL
*/
static void
mrbc_esp32_httpclient_init(mrb_vm* vm, mrb_value* v, int argc)
{
  char* url = (char*)GET_STRING_ARG(1);
  esp_http_client_config_t config = {
    .url = url,
    .event_handler = http_event_handler
  };
  client = esp_http_client_init(&config);
}


/*! メソッド set_method_post() 本体 : wrapper for esp_http_client_set_method
  HTTP METHOD を POST に設定する
  引数なし
*/
static void
mrbc_esp32_httpclient_set_method_post(mrb_vm* vm, mrb_value* v, int argc)
{
  esp_http_client_set_method(client, HTTP_METHOD_POST);
}


/*! メソッド set_header() 本体 : wrapper for esp_http_client_set_header
  HTTP ヘッダを１つセットする

  @param key ヘッダのキー文字列
  @param val ヘッダの値文字列
*/
static void
mrbc_esp32_httpclient_set_header(mrb_vm* vm, mrb_value* v, int argc)
{
  char* key = (char*)GET_STRING_ARG(1);
  char* val = (char*)GET_STRING_ARG(2);
  esp_http_client_set_header(client, key, val);
}


/*! メソッド set_body() 本体 : wrapper for esp_http_client_set_post_field
  HTTP メッセージボディをセットする

  @param data メッセージボディ文字列
*/
static void
mrbc_esp32_httpclient_set_body(mrb_vm* vm, mrb_value* v, int argc)
{
  char* data = (char*)GET_STRING_ARG(1);
  int   len  = strlen(data);
  esp_http_client_set_post_field(client, data, len);
}


/*! メソッド invoke() 本体 : wrapper for esp_http_client_perform
  引数なし
*/
static void
mrbc_esp32_httpclient_invoke(mrb_vm* vm, mrb_value* v, int argc)
{
  esp_err_t err = esp_http_client_perform(client);
  if (err != ESP_OK) {
    ESP_LOGE(tag, "HTTP GET request failed: %s", esp_err_to_name(err));
  }else{
  }
}


/*! メソッド cleanup() 本体 : wrapper for esp_http_client_cleanup
  引数なし
*/
static void
mrbc_esp32_httpclient_cleanup(mrb_vm* vm, mrb_value* v, int argc)
{
  esp_http_client_cleanup(client);
}


/*! クラス定義処理を記述した関数
  この関数を呼ぶことでクラス HTTPClient が定義される

  @param vm mruby/c VM
*/
void
mrbc_mruby_esp32_httpclient_gem_init(struct VM* vm)
{
/*
HTTPClient.init("http://foo.bar/)
HTTPClient.invoke()
HTTPClient.cleanup()
*/

  // クラス HTTPClient 定義
  mrbc_class_esp32_httpclient = mrbc_define_class(vm, "HTTPClient", mrbc_class_object);
  // 各メソッド定義
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "init",       mrbc_esp32_httpclient_init);
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "set_method_post", mrbc_esp32_httpclient_set_method_post);
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "set_header", mrbc_esp32_httpclient_set_header);
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "set_body",   mrbc_esp32_httpclient_set_body);
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "invoke",     mrbc_esp32_httpclient_invoke);
  mrbc_define_method(vm, mrbc_class_esp32_httpclient, "cleanup",    mrbc_esp32_httpclient_cleanup);
}
